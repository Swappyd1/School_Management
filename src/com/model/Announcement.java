package com.model;

public class Announcement
{
	private int anid;
	private String annname, dateofan;
	public int getAnid() {
		return anid;
	}
	public void setAnid(int anid) {
		this.anid = anid;
	}
	public String getAnnname() {
		return annname;
	}
	public void setAnnname(String anname) {
		this.annname = anname;
	}
	public String getDateofan() {
		return dateofan;
	}
	public void setDateofan(String dateofan) {
		this.dateofan = dateofan;
	}

}
